package bean;

public class Dun {
	
	private int id;
	private Parlimen parlimen;
	private String name;
	private String code;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Parlimen getParlimen() {
		return parlimen;
	}
	public void setParlimen(Parlimen parlimen) {
		this.parlimen = parlimen;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}

}
