package bean;

public class Koku {
	private String kokuId;
	private KokuType koku_Type;
	private String kokuName;
	public String getKokuId() {
		return kokuId;
	}
	public void setKokuId(String kokuId) {
		this.kokuId = kokuId;
	}
	public KokuType getKoku_Type() {
		return koku_Type;
	}
	public void setKoku_Type(KokuType koku_Type) {
		this.koku_Type = koku_Type;
	}
	public String getKokuName() {
		return kokuName;
	}
	public void setKokuName(String kokuName) {
		this.kokuName = kokuName;
	}
	
	
}
